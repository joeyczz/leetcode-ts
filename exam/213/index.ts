function rob11(nums: number[], map: { [key: number]: number }): number {
  const len = nums.length;
  if (nums[len - 1] === 0) return rob11(nums.slice(0, len - 1), map);
  if (map[len] !== undefined) return map[len];
  if (len <= 0) return 0;
  if (len === 1) {
    map[1] = nums[0];
    return nums[0];
  }
  if (len === 2) {
    map[2] = Math.max(nums[0], nums[1]);
    return Math.max(nums[0], nums[1]);
  }

  const last = rob11(nums.slice(0, len - 2), map) + nums[len - 1];
  const lastP = rob11(nums.slice(0, len - 3), map) + nums[len - 2];
  map[len] = Math.max(last, lastP);
  return Math.max(last, lastP);
}

function rob1(nums: number[]): number {
  const len = nums.length;
  if (len <= 0) return 0;
  if (len === 1) return nums[0];
  if (len === 2) return Math.max(nums[0], nums[1]);
  const map0 = {};
  const last = rob11(nums.slice(1, len - 2), {}) + nums[len - 1];
  const lastP1 = rob11(nums.slice(0, len - 2), map0);
  const lastP = rob11(nums.slice(0, len - 3), map0) + nums[len - 2];
  return Math.max(last, lastP1, lastP);
}
